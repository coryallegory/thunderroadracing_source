	In November of 2004, Ross picked up a basket case Junior Scout from a fellow collector in Regina, Saskatchewan. After 6 months and a little raiding of Metcalfe's parts department, the racer was born under the direction of Ted and Gary who made anything that was missing on the lathe.
	<p>
	Soon after, the Thunder Road Racer was unveiled, and Ted was off and riding around the neighbourhood after only a couple of kicks. Gary also took it for a spin, and it was noticed that oil return seemed to be a problem as the pressure was low - it seems a screw was missing in the mag. The problem was fixed and then we had full pressure.
	<p>
	After a couple of weeks of short rides by Ted, the Indian was ready for its first high-speed test run.
